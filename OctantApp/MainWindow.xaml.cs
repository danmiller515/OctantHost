        private bool _closeStoryBoardCompleted = false;
        private bool _restoreIfMove = false;

        public MainWindow()
        {
            if (!Cef.IsInitialized)
            {
                var settings = new CefSettings();
                settings.LogSeverity = LogSeverity.Verbose;
                settings.CachePath = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData), "Octant\\CefSharp\\Cache");
                settings.SetOffScreenRenderingBestPerformanceArgs();
                Cef.Initialize(settings);
            }

            this.DataContext = this;

            InitializeComponent();

        

            WebBrowser.MenuHandler = new WebBrowserMenuHandler();

            this.SourceInitialized += new EventHandler(win_SourceInitialized);
        }

        private void Window_Loaded(object sender, RoutedEventArgs e)
        {
            WebBrowser.KeyDown += new KeyEventHandler(WebBrowser_KeyDown);
        }

        private void Window_Closing(object sender, System.ComponentModel.CancelEventArgs e)
        {
            if (!_closeStoryBoardCompleted)
            {
                Storyboard s = (Storyboard)TryFindResource("close");
                s.Begin(this);
                e.Cancel = true;
            }
        }

        private void WebBrowser_IsBrowserInitializedChanged(object sender, DependencyPropertyChangedEventArgs e)
        {
            if (WebBrowser.IsBrowserInitialized == true)
            {
                WebBrowser.Load("http://localhost:7777");
            }
        }

        void WebBrowser_KeyDown(object sender, KeyEventArgs e)
        {
            if (e.Key == Key.F5)
            {
                WebBrowser.Reload();
            }
        }

        private void closeStoryBoard_Completed(object sender, EventArgs e)
        {
            _closeStoryBoardCompleted = true;
            this.Close();
        }

        private void btnClose_Click(object sender, RoutedEventArgs e)
        {
            this.Close();
        }

        private void btnMinimize_Click(object sender, RoutedEventArgs e)
        {
            WindowState = WindowState.Minimized;
        }

        private void btnMaximise_Click(object sender, RoutedEventArgs e)
        {
            if(WindowState == WindowState.Maximized)
            {
                WindowState = WindowState.Normal;
            }
            else
            {
                WindowState = WindowState.Maximized;
            }
        }

        private void SwitchWindowState()
        {
            switch (WindowState)
            {
                case WindowState.Normal:
                    {
                        WindowState = WindowState.Maximized;
                        break;
                    }
                case WindowState.Maximized:
                    {
                        WindowState = WindowState.Normal;
                        break;
                    }
            }
        }


        private void rctHeader_MouseLeftButtonDown(object sender, MouseButtonEventArgs e)
        {
            if (e.ClickCount == 2)
            {
                if ((ResizeMode == ResizeMode.CanResize) || (ResizeMode == ResizeMode.CanResizeWithGrip))
                {
                    SwitchWindowState();
                }

                return;
            }

            else if (WindowState == WindowState.Maximized)
            {
                _restoreIfMove = true;
                return;
            }

            DragMove();
        }


        private void rctHeader_MouseLeftButtonUp(object sender, MouseButtonEventArgs e)
        {
            _restoreIfMove = false;
        }


        private void rctHeader_MouseMove(object sender, MouseEventArgs e)
        {
            if (_restoreIfMove)
            {
                _restoreIfMove = false;

                double percentHorizontal = e.GetPosition(this).X / ActualWidth;
                double targetHorizontal = RestoreBounds.Width * percentHorizontal;

                double percentVertical = e.GetPosition(this).Y / ActualHeight;
                double targetVertical = RestoreBounds.Height * percentVertical;

                WindowState = WindowState.Normal;

                POINT lMousePosition;
                GetCursorPos(out lMousePosition);

                Left = lMousePosition.X - targetHorizontal;
                Top = lMousePosition.Y - targetVertical;

                DragMove();
            }
        }

        [DllImport("user32.dll")]
        [return: MarshalAs(UnmanagedType.Bool)]
        static extern bool GetCursorPos(out POINT lpPoint);

